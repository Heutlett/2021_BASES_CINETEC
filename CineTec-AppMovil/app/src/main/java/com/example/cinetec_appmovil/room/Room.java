package com.example.cinetec_appmovil.room;

public class Room {


    private int rows;
    private int columns;


    public Room(int rows, int columns) {
        this.rows = rows;
        this.columns = columns;
    }

    public int getRows() {
        return rows;
    }

    public int getColumns() {
        return columns;
    }
}
